    public bool InsertAnal(string cocode,string analcode,string analname,string yearcode,string userid,string analtype)
    {
        try
        {
            // Connection.
            theConnection = new SqlConnection();
            if (conString != "")
            {
                theConnection.ConnectionString = conString;
                theConnection.Open();
            }

            // Command.
            theCommand = new SqlCommand();
            theCommand.Connection = theConnection;


            using (IDbTransaction tran = theConnection.BeginTransaction())
            {
                try
                {
                    // transactional code...
                    using (theCommand = theConnection.CreateCommand())
                    {
                        theCommand.CommandText = "INSERT INTO Analmast(CompCode,analCode,analname,Tag,User01,LogDt01, CFiller01,CFiller02) VALUES('" + cocode + "','" + analcode + "','" + analname + "','1','" + userid + "','"+DateTime.Now.ToString("MM/dd/yyyy")+"','" + yearcode + "','"+analtype+"')";
                        theCommand.Transaction = tran as SqlTransaction;
                        theCommand.ExecuteNonQuery(); // Execute insert query. 
                    }
                    tran.Commit();
                }
                catch
                {
                    tran.Rollback();
                    throw;
                }
            }
            return true;
        }
        catch
        {
            return false;
        }
        finally
        {
            theConnection.Close();
            theCommand.Dispose();
        }
    }

