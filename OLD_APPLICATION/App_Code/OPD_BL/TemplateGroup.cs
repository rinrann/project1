            theCommand.Connection = theConnection;

            theCommand.CommandText = "update IPD_PrescriptionTmplateGroup set PrescriptionGroupName='" + name + "' where RowId = '" + id + "'and compcode='" + cocode + "'";
            theCommand.CommandType = CommandType.Text;

